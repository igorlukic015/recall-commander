    [Fact]
    public void Empty_answers_are_valid()
    {
        var markdown =
            $"""
            {Frontmatter}

            ## Question 1

            First prompt?

            ### Answer

            ---

            ## Question 2

            Second prompt?

            ### Answer
            """;

        var result = _parser.Parse(markdown);

        Assert.True(result.IsValid);
        Assert.All(result.Attempt.Questions, question =>
        {
